  end

  depends_on "cmake" => :build

  def install
    if build.stable?
      cd "cpp"
    end

    mkdir "build" do
      system "cmake", "..", *std_cmake_args
      system "make"

      if build.stable?
        lib.install "libzxing.a"
        bin.install "zxing"

        cd "../core/src/"
        Dir.glob "./zxing/**/*.h" do |header|
          (include/File.dirname(header)).install header
        end
      end

      if build.head?
        system "make", "install"
      end
    end
  end

