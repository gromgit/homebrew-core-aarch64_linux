    sha256 "237597cd7d2517d3b34144ba0eacd21a9d50d32e6bbe0ce496eaf8d1b9c9dab7" => :high_sierra
  end

  depends_on "libgit2"
  depends_on "python@3.8"

  uses_from_macos "libffi"

  resource "cached-property" do
